package com.slyworks.a07_command_pattern;

public class GarageDoor {
    public void up(){
        System.out.println("Garage Door was raised UP");
    }
    public void down(){
        System.out.println("Garage Door was brought DOWN");
    }
    public void stop(){
        System.out.println("Garage Door was movement was STOPPED");
    }

}
